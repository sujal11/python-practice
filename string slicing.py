name="SUJAL"
print(name[0:3]) # --> printing the string starting from index 0 upto 3 i.e. 0,1,2
print(name[2])   # --> printing the element present at index 2 of string
print(name[:3])  # --> it is automatically assuming that starting index is 0 same as in name[0:3]
print(name[0:4:2]) # -->this will escape every 2nd value of string as we declared 2 in the print statement as 2 arguement 