a = "sujal is a good boy"
# string functions:
print(len(a))       # --> len() is printing the length of string
print(a.endswith('boy')) # --> .endswith() reperesents the string ending by giving true or false value
print(a.count("o"))  # --> .count() functions counts the occurence of alphabet/string 
print(a.capitalize()) # --> .capitalize() captalize the first alphabet of string  
print(a.find('good')) # --> finds the word and print there index 
print(a.replace("good","bad")) # --> .replace() replaces the word [].replace("old word","new word")]